length = int(input().strip())

if 620 <= length <= 780:
    print("Red")
elif 590 <= length < 620:
    print("Orange")
elif 570 <= length < 590:
    print("Yellow")
elif 495 <= length < 570:
    print("Green")
elif 450 <= length < 495:
    print("Blue")
elif 425 <= length < 450:
    print("Indigo")
elif 380 <= length < 425:
    print("Violet")